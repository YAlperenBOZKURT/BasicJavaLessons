public class EscapeSequences {
    public static void main(String[] args) {

        //   \n: Newline character. It represents a line break, moving to the next line.
        // \t: Tab character. Used for formatting text or creating specific spaces.
        //  \: Backslash. Used to include a backslash within a string.
        // ": Double quotation mark. Used to include a double quotation mark within a string.
        //  ': Single quotation mark. Used to include a single quotation mark within a character literal.

        System.out.println("Hello, world!\nThis is a new line.");
        System.out.println("Using \ttab in this text.");
        System.out.println("This \"quote\" will be displayed.");
        System.out.println("Displaying backslash: \\");
        System.out.println("Single quote: \'");


    }

}
