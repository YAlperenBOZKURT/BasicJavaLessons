public class Operators {
    public static void main(String[] args) {

        // Arithmetic Operators:

        int x = 10;
        int y = 4;
        int sum = x + y;         // 14
        int difference = x - y;  // 6
        int product = x * y;     // 40
        int quotient = x / y;    // 2
        int remainder = x % y;   // 2

        // Assignment Operators:

        int a = 5;
        a += 3;   // a is now 8
        a -= 2;   // a is now 6
        a *= 4;   // a is now 24
        a /= 3;   // a is now 8

        // Comparison Operators:

        int p = 5;
        int q = 3;
        boolean isEqual = (p == q);          // false
        boolean isNotEqual = (p != q);       // true
        boolean isGreater = (p > q);         // true
        boolean isLess = (p < q);            // false
        boolean isGreaterOrEqual = (p >= q); // true
        boolean isLessOrEqual = (p <= q);    // false

        // Logical Operators:

        boolean condition1 = true;
        boolean condition2 = false;
        boolean result1 = condition1 && condition2;   // false
        boolean result2 = condition1 || condition2;   // true
        boolean result3 = !condition1;                // false





    }

}
