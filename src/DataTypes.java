public class DataTypes {
    public static void main(String[] args) {

        // int data type - Represents integers
        int number = 5;
        System.out.println("int number: " + number);

        // double data type - Represents double-precision floating-point numbers
        double pi = 3.14;
        System.out.println("double pi: " + pi);

        // float data type - Represents single-precision floating-point numbers
        float temperature = 25.5f;
        System.out.println("float temperature: " + temperature);

        // char data type - Represents a single character
        char grade = 'A';
        System.out.println("char grade: " + grade);

        // boolean data type - Represents logical values (true or false)
        boolean isPassed = true;
        System.out.println("boolean isPassed: " + isPassed);

        // byte data type - Represents small integers
        byte smallNumber = 10;
        System.out.println("byte smallNumber: " + smallNumber);

        // short data type - Represents short integers
        short year = 2023;
        System.out.println("short year: " + year);

        // long data type - Represents large integers
        long bigNumber = 1234567890L;
        System.out.println("long bigNumber: " + bigNumber);
    }
}
